'''Write a Python program to iterate through both the keys and values of a dictionary and print
them '''

capitals_info={"USA": "Washington DC", "India": "New Delhi", "UAE": "Abu Dhabi",
               "Canada": "Ottawa", "Australia": "Canberra", "Indonesia": "Jakarta", }

x=capitals_info.keys()
y=capitals_info.values()

print("country names: ", x)
print("country's capitals: ", y)

